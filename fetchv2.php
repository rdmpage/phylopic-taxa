	$image_filename = '';

	$prefix = substr($name, 0, 1);
	
	$destination_dir = $base_dir . '/' . $prefix;
	
	if (!file_exists($destination_dir))
	{
		$oldumask = umask(0); 
		mkdir($destination_dir, 0777);
		umask($oldumask);
	}
	
	$image_filename = $destination_dir . '/' . $name  . '.' . $extension; 
	
	return $image_filename;

}

